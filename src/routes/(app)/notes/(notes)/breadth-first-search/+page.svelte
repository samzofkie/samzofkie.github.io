<h1>Breadth-first search</h1>

<p>
  Given a graph <math>G</math> and a source node <math>s</math>, <b>breadth-first search</b> systematically discovers every vertex available from <math>s</math>, producing a "breadth-first tree", where <math>s</math> is the root and the simplest path in the tree from <math>s</math> to an arbitrary vertex <math>v</math> corresponds to the shortest walk from <math>s</math> to <math>v</math>in <math>G</math>.
</p>